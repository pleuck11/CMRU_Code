def bubble_sort(A):
    n = len(A)
    for i in range(n - 1):  # วนลูปผ่านอาร์เรย์ n-1 รอบ
        for j in range(n - 1 - i):  # วนลูปเปรียบเทียบและสลับค่า
            if A[j + 1] < A[j]:  # ถ้าค่าปัจจุบันมากกว่าค่าถัดไป ให้สลับที่
                A[j], A[j + 1] = A[j + 1], A[j]

# ทดสอบการทำงาน
arr = [64, 34, 25, 12, 22, 11, 90]
bubble_sort(arr)
print(arr)  # [11, 12, 22, 25, 34, 64, 90]

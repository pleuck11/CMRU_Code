def insertion_sort(A):
    n = len(A)
    for i in range(1, n):  # วนลูปจากตัวที่สองจนถึงตัวสุดท้าย
        v = A[i]  # เก็บค่าปัจจุบันที่ต้องนำไปแทรก
        j = i - 1  # เริ่มตรวจสอบจากตำแหน่งก่อนหน้า

        while j >= 0 and A[j] > v:  # เลื่อนค่าที่มากกว่า v ไปขวา
            A[j + 1] = A[j]
            j -= 1

        A[j + 1] = v  # นำ v ไปใส่ที่ตำแหน่งที่เหมาะสม

# ทดสอบการทำงาน
arr = [64, 34, 25, 12, 22, 11, 90]
insertion_sort(arr)
print(arr)  # [11, 12, 22, 25, 34, 64, 90]
